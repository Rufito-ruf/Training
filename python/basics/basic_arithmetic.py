left = float(input("Enter the first number: "))
right = float(input("Enter the second number: "))

sum = left + right
dif = left - right
prod = left * right
quot = left / right

print("Sum of {0} and {1} equals {2}".format(left, right, sum))
print("Difference between {0} and {1} equals {2}".format(left, right, dif))
print("Product of {0} and {1} equals {2}".format(left, right, prod))
print("Quotient of {0} and {1} equals {2}".format(left, right, quot))
